const ENDPOINT = "cars.json";

async function getCars() {
  try {
    const response = await fetch(ENDPOINT);

    if (!response.ok) {
      throw new Error(`Error! status: ${response.status}`);
    }

    const result = await response.json();
    return result;
  } catch (err) {
    console.log(err);
  }
  console.log(result);
}
const carList = getCars();
